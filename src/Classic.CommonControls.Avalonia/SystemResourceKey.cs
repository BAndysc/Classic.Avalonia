public class SystemResourceKey
{
    internal SystemResourceKey(SystemResourceKeyID keyId)
    {
        this.keyId = keyId;
    }

    public override int GetHashCode()
    {
        return (int)keyId;
    }

    public override bool Equals(object? o)
    {
        if (o is SystemResourceKey key)
        {
            return (keyId == key.keyId);
        }

        return false;
    }

    public override string ToString()
    {
        return keyId.ToString();
    }

    private readonly SystemResourceKeyID keyId;
}